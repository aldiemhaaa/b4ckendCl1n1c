const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const MoneySchema = new Schema({
  description: {
    type: String,
    required: true
  },
  amount: {
    type: Number,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.model('money', MoneySchema);
